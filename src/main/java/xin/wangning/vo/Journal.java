package xin.wangning.vo;

public class Journal {
    String name;
    String url;
    Long rank;
    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

	public Long getRank() {
		return rank;
	}

	public void setRank(Long rankLong) {
		this.rank = rankLong;
	}
    
}
